n=int(input())
i=1
while(i < n+1):
    if i==1:
        print(i, "minute=", i*60, "seconds")
    else:
        print(i, "minutes=", i*60, "seconds")
    i += 1
 
#i is for incrementation
# n+1 so it runs till from 1 till n
# if else statement is for singular plural
